package fhw;

public class Frog
{
    private String name;     
    private FrogTypeEnum type; 
    
    public Frog() {}

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public FrogTypeEnum getType()
    {
        return type;
    }

    public void setType(FrogTypeEnum type)
    {
        this.type = type;
    }
        
    
}
